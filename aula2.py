# CALCULADORA

import math
a = int(input("Digte o primeiro número: "))
b = int(input("Digte o segundo número: "))

soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
raiz = math.sqrt(a)

print("Soma: " + str(soma))
print("Subtracao: " + str(subtracao))
print("Multiplicacao: " + str(multiplicacao))
print("Divisao: " + str(divisao))
print("Raiz: " + str(raiz))